package com.beauty.goshop.model;

public class Emina {
    private String product;
    private String detail;
    private int image;
    private String spesifikasiHarga;
    private String spesifikasiKategori;
    private int spesifikasiStok;

    public String getSpesifikasiHarga() {
        return spesifikasiHarga;
    }

    public void setSpesifikasiHarga(String spesifikasiHarga) {
        this.spesifikasiHarga = spesifikasiHarga;
    }

    public String getSpesifikasiKategori() {
        return spesifikasiKategori;
    }

    public void setSpesifikasiKategori(String spesifikasiKategori) {
        this.spesifikasiKategori = spesifikasiKategori;
    }

    public int getSpesifikasiStok() {
        return spesifikasiStok;
    }

    public void setSpesifikasiStok(int spesifikasiStok) {
        this.spesifikasiStok = spesifikasiStok;
    }

    public String getProduct() {
        return product;
    }

    public void setProduct(String product) {
        this.product = product;
    }

    public String getDetail() {
        return detail;
    }

    public void setDetail(String detail) {
        this.detail = detail;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }
}
